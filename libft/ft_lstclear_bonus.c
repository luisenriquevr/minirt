#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list *now;
	t_list *after;

	now = *lst;
	while (now)
	{
		after = now->next;
		del(now->content);
		free(now);
		now = after;
	}
	*lst = NULL;
}
